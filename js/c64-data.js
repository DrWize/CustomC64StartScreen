    // Build full 4096-byte chargen ROM: 2 sets × 256 chars × 8 bytes
    function buildChargenROM() {
        const rom = new Uint8Array(4096);

        // Set 1: uppercase/graphics
        for (let i = 0; i < 1024; i++) rom[i] = CHARSET_UPPER[i];
        // Set 1 reverse (128-255): invert bytes
        for (let i = 0; i < 1024; i++) rom[1024 + i] = CHARSET_UPPER[i] ^ 0xFF;

        // Set 2: lowercase/uppercase
        for (let i = 0; i < 1024; i++) rom[2048 + i] = CHARSET_LOWER[i];
        // Set 2 reverse
        for (let i = 0; i < 1024; i++) rom[3072 + i] = CHARSET_LOWER[i] ^ 0xFF;

        return rom;
    }

    // ── PETSCII to Screen Code conversion ───────────────────────────────
    function petsciiToScreenCode(petscii) {
        if (petscii < 32) return petscii + 128;
        if (petscii < 64) return petscii;
        if (petscii < 96) return petscii - 64;
        if (petscii < 128) return petscii - 32;
        if (petscii < 160) return petscii + 64;
        if (petscii < 192) return petscii - 64;
        if (petscii < 255) return petscii - 128;
        return 94; // pi
    }

    function screenCodeToPetscii(sc) {
        if (sc < 32) return sc + 64;
        if (sc < 64) return sc;
        if (sc < 96) return sc + 32;
        if (sc < 128) return sc + 64;
        return sc;
    }

    // ASCII to screen code (for text input convenience)
    function asciiToScreenCode(ascii) {
        if (ascii >= 65 && ascii <= 90) return ascii - 64;   // A-Z → 1-26
        if (ascii >= 97 && ascii <= 122) return ascii - 96;  // a-z → 1-26
        if (ascii >= 32 && ascii <= 63) return ascii;        // space-? direct
        if (ascii === 64) return 0;                           // @ → 0
        return 32; // default: space
    }

    // Convert ASCII string to screen codes
    function stringToScreenCodes(str) {
        const codes = [];
        for (let i = 0; i < str.length; i++) {
            codes.push(asciiToScreenCode(str.charCodeAt(i)));
        }
        return codes;
    }

    // Screen code to displayable ASCII char (approximate)
    function screenCodeToChar(sc) {
        sc = sc & 0x7F; // strip reverse bit
        if (sc === 0) return '@';
        if (sc >= 1 && sc <= 26) return String.fromCharCode(sc + 64);
        if (sc >= 27 && sc <= 31) return ' '; // special chars
        if (sc >= 32 && sc <= 63) return String.fromCharCode(sc);
        return ' '; // graphics
    }

